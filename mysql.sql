DROP DATABASE db;
CREATE DATABASE db;
USE db;

CREATE TABLE DHMOTHS(
	DNAME VARCHAR(20) DEFAULT "ABAPTISTOS",
	DSURNAME VARCHAR(30) NOT NULL,
	AMKA INT NOT NULL,
	PRIMARY KEY (AMKA)
	);
    
CREATE TABLE YPHRESIA(
	TITLE char(25) NOT NULL,
	DAYS INT NOT NULL,
	ID INT(3) NOT NULL,
	PRIMARY KEY (ID)
	);
    
CREATE TABLE MERIDA(
	MDATE DATE NOT NULL,
	POP INT,
	MER INT(5) NOT NULL,
	PRIMARY KEY (MER)
	);
